package com.transportmanagementfrontend;

import java.io.Serializable;

public class UserParameter implements Serializable {
    private int id;
    private String firstName;
    private String lastName;
    private int age;
    private String address;
    private String username;
    private String password;
    private String role;
    private String phone;
    private String otp;

    // Default Constructor
    public UserParameter() {
    }

    // Parameterized Constructor
    public UserParameter(int id, String firstName, String lastName, int age, String address,
                         String username, String password, String role, String phone, String otp) {
        this.id = id;
        this.firstName = firstName;
        this.lastName = lastName;
        this.age = age;
        this.address = address;
        this.username = username;
        this.password = password;
        this.role = role;
        this.phone = phone;
        this.otp = otp;
    }

    // Getters & Setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getOtp() {
        return otp;
    }

    public void setOtp(String otp) {
        this.otp = otp;
    }
}
